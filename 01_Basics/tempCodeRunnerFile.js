let a = 100;       // integer
let b = 3.14;      // floating point
let c = -200;      // negative number
let d = 1e6;       // exponential notation (1 million)
let e = Infinity;  // special number
let f = NaN;       // special value representing 'not a number'

console.table({
    "Value of a": a,
    "Type of a": typeof a,
    "Value of b": b,
    "Type of b": typeof b,
    "Value of c": c,
    "Type of c": typeof c,
    "Value of d": d,
    "Type of d": typeof d,
    "Value of e": e,
    "Type of e": typeof e,
    "Value of f": f,
    "Type of f": typeof f
});
